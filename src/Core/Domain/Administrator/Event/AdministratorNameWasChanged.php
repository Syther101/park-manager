namespace ParkManager\Core\Domain\Administrator\Event;

use ParkManager\Component\SharedKernel\Event\DomainEvent;
use ParkManager\Component\User\Model\UserId;

class AdministratorNameWasChanged extends DomainEvent
{
    private $id;
    private $firstName;
    private $lastName;

    public function __construct(UserId $id, string $firstName, string $lastName)
    {
        $this->id = $id;
        $this->firstName = $firstName;
        $this->lastName = $lastName;
    }

    public function id(): UserId
    {
        return $this->id;
    }

    public function firstName(): string
    {
        return $this->firstName;
    }

    public function lastName(): string
    {
        return $this->lastName;
    }
}
